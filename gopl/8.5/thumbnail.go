package thumbnail

import (
	"log"
	"os"
	"sync"

	"gopl.io/ch8/thumbnail"
)

// makeThumbnails makes thumbnails of the specified files.
func makeThumbnails(filenames []string) {
	for _, f := range filenames {
		if _, err := thumbnail.ImageFile(f); err != nil {
			log.Println(err)
		}
	}
}

